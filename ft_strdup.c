#include "libft.h"
#include <stdlib.h>

char *ft_strdup(const char *s)
{
	size_t		i;
	char		*copy;

	i = ft_strlen(s);
	copy = malloc(sizeof(*copy) * (i + 1));
	if (!copy)
		return (NULL);
	i = 0;
	while (s[i])
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = s[i];
	return (copy);
}
